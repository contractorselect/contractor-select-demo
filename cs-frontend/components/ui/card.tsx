import * as React from 'react';
import { cn } from '@/lib/utils';

/**
 * Card — base container per Design System §6.3.
 * White surface, subtle border, default shadow-md elevation.
 */
export const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & { interactive?: boolean }
>(({ className, interactive, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      'rounded-lg bg-white border border-neutral-100 shadow-sm',
      interactive && 'transition-shadow hover:shadow-md cursor-pointer',
      className,
    )}
    {...props}
  />
));
Card.displayName = 'Card';

